$(".hoverAfrica").hover(function(){
    $("#hover_africa").css("top","63px")
    $("#menu_africa").css("background-image", "linear-gradient(to right,#4F049D,#CB1FDD)")
    $("#menu_africa").css("cursor","pointer")
    $("#menu_africa").css("color","white")
},function(){
    $("#hover_africa").css("top","-400px")
    $("#menu_africa").css("background", "white")
    $("#menu_africa").css("cursor","none")
    $("#menu_africa").css("color","black")
})

